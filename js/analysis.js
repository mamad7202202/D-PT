/* ============================================================
   D-PT Analysis Generator
   Builds a serious, meaningful report + humor layer derived
   strictly from the actual normalized scores.
   ============================================================ */

const Analysis = (() => {

  const hi = (n, k) => n[k] >= 62;
  const lo = (n, k) => n[k] <= 38;
  const mid = (n, k) => !hi(n,k) && !lo(n,k);

  /** Main entry: produce full report object. */
  function build(scores) {
    const n = scores.normalized;
    return {
      type: Engine.deriveType(n),
      summary: summary(n),
      strengths: strengths(n),
      growth: growth(n),
      workStyle: workStyle(n),
      learningStyle: learningStyle(n),
      decision: decision(n),
      archetype: archetype(n),
      funny: funny(n),
      scores: n,
    };
  }

  /* ---------- Serious sections ---------- */

  function summary(n) {
    const parts = [];
    parts.push(
      hi(n,"E") ? "تو فردی برون‌گرا هستی که از تعامل و حضور در جمع انرژی می‌گیری."
      : lo(n,"E") ? "تو بیشتر درون‌گرا هستی و عمق تنهایی و تأمل برایت ارزشمندتر از شلوغی است."
      : "تو تعادلی میان درون‌گرایی و برون‌گرایی داری و بسته به موقعیت رفتارت تغییر می‌کند."
    );
    parts.push(
      hi(n,"O") ? "ذهنت به‌شدت پذیرای ایده‌های نو و تجربه‌های تازه است."
      : lo(n,"O") ? "به امور عملی و آزموده‌شده بیش از انتزاع و آزمایش علاقه داری."
      : "ترکیبی از واقع‌گرایی و گشودگی نسبت به ایده‌های نو در تو دیده می‌شود."
    );
    parts.push(
      hi(n,"C") ? "نظم و وظیفه‌شناسی از ستون‌های شخصیت توست و کارها را تا انتها می‌بری."
      : lo(n,"C") ? "روحی آزاد و خودجوش داری؛ ساختار سفت‌و‌سخت برایت دست‌و‌پاگیر است."
      : "می‌توانی هم منظم باشی و هم در صورت نیاز انعطاف نشان دهی."
    );
    parts.push(
      hi(n,"S") ? "در برابر فشار، آرامش و ثبات هیجانی قابل‌توجهی از خود نشان می‌دهی."
      : lo(n,"S") ? "حساسیت هیجانی بالایی داری؛ این هم منبع همدلی توست و هم گاهی منبع اضطراب."
      : "واکنش‌های هیجانی‌ات معمولاً متناسب با موقعیت و قابل‌مدیریت است."
    );
    return parts.join(" ");
  }

  function strengths(n) {
    const pool = [
      [hi(n,"CR"), "تولید ایده‌های خلاقانه و دیدن راه‌حل‌هایی که از چشم دیگران پنهان است."],
      [hi(n,"L"),  "توانایی برعهده‌گرفتن مسئولیت و هدایت دیگران در شرایط مبهم."],
      [hi(n,"ST"), "نگاه استراتژیک و توانایی دیدن تصویر بزرگ پیش از دیگران."],
      [hi(n,"DI"), "انضباط شخصی بالا و پایبندی به اهداف حتی بدون انگیزهٔ بیرونی."],
      [hi(n,"CU"), "کنجکاوی سیری‌ناپذیر که موتور یادگیری مداوم توست."],
      [hi(n,"S"),  "آرامش و تاب‌آوری بالا در برابر فشار و بحران."],
      [hi(n,"AD"), "انعطاف‌پذیری و توانایی وفق‌یافتن سریع با تغییرات."],
      [hi(n,"E"),  "انرژی اجتماعی بالا و توانایی ساختن ارتباط با آدم‌های مختلف."],
      [hi(n,"D"),  "تصمیم‌گیری منطقی و مبتنی بر تحلیل به‌جای هیجان لحظه‌ای."],
      [hi(n,"O"),  "ذهن باز و استقبال از ایده‌ها و دیدگاه‌های متفاوت."],
      [hi(n,"R"),  "شهامت پذیرش ریسک‌های حساب‌شده برای دستیابی به فرصت‌های بزرگ."],
      [lo(n,"E"),  "توانایی تمرکز عمیق و کار مستقل بدون نیاز به تأیید مداوم."],
    ];
    return pick(pool, 3, 6, "تعادل کلی شخصیت که در موقعیت‌های متنوع کارآمد است.");
  }

  function growth(n) {
    const pool = [
      [lo(n,"DI"), "تکمیل کارهایی که با شور شروع می‌شوند اما نیمه‌کاره می‌مانند."],
      [lo(n,"C"),  "ساختن نظم و روتین برای جلوگیری از پراکندگی انرژی."],
      [hi(n,"CR")&&lo(n,"DI"), "تبدیل سیل ایده‌ها به خروجی‌های واقعی و تمام‌شده."],
      [lo(n,"S"),  "مدیریت اضطراب و کاهش درگیری ذهنی با نگرانی‌های فرضی."],
      [hi(n,"S")&&lo(n,"D"), "اعتماد بیشتر به تحلیل منطقی در کنار شهود."],
      [lo(n,"AD"), "افزایش انعطاف در برابر تغییرات ناگهانی برنامه."],
      [hi(n,"R")&&lo(n,"C"), "افزودن کمی محاسبه و احتیاط پیش از ریسک‌های بزرگ."],
      [lo(n,"E"),  "تمرین حضور فعال‌تر در جمع وقتی موقعیت اقتضا می‌کند."],
      [hi(n,"E")&&lo(n,"DI"), "تبدیل انرژی اجتماعی به تمرکز پایدار روی اولویت‌ها."],
      [hi(n,"O")&&lo(n,"DI"), "تمرکز روی چند هدف کلیدی به‌جای پراکندگی میان موضوعات."],
      [lo(n,"L"),  "تمرین ابراز نظر و برعهده‌گرفتن ابتکار در گروه."],
      [hi(n,"C")&&lo(n,"AD"), "رهاکردن کمال‌گرایی افراطی و پذیرش «به‌اندازهٔ کافی خوب»."],
    ];
    return pick(pool, 3, 6, "تعمیق نقاط قوت فعلی و تمرین خروج آگاهانه از منطقهٔ امن.");
  }

  function workStyle(n) {
    let collab = hi(n,"E")
      ? "در محیط‌های تیمی و پرتعامل می‌درخشی و از هم‌اندیشی انرژی می‌گیری."
      : lo(n,"E") ? "بهترین کارت را در فضای آرام و مستقل ارائه می‌دهی."
      : "می‌توانی هم در تیم و هم به‌تنهایی مؤثر باشی.";
    let lead = hi(n,"L")
      ? "گرایش طبیعی به رهبری داری و در موقعیت‌های مبهم جلو می‌افتی."
      : lo(n,"L") ? "ترجیح می‌دهی نقش متخصص و پشتیبان قابل‌اتکا را ایفا کنی تا رهبر رسمی."
      : "بسته به موقعیت، هم رهبری را می‌پذیری و هم همراهی می‌کنی.";
    let focus = hi(n,"DI")
      ? "تمرکز پایدار و توانایی پیگیری بلندمدت از ویژگی‌های کاری توست."
      : lo(n,"DI") ? "در فازهای آغازین و خلاقانه قوی هستی اما پیگیری بلندمدت چالش توست."
      : "تمرکزت متعادل است و با کمی ساختار، بهره‌وری‌ات اوج می‌گیرد.";
    return [collab, lead, focus].join(" ");
  }

  function learningStyle(n) {
    let proc = hi(n,"O")||hi(n,"CU")
      ? "اطلاعات را مفهومی و در ارتباط با تصویر بزرگ پردازش می‌کنی."
      : "یادگیری ملموس، گام‌به‌گام و کاربردی برایت مؤثرتر است.";
    let solve = hi(n,"D")
      ? "در حل مسئله، تحلیل منطقی و داده‌محور را مقدم می‌داری."
      : hi(n,"CR") ? "در حل مسئله به شهود و راه‌حل‌های خلاقانه تکیه می‌کنی."
      : "ترکیبی از منطق و شهود را در حل مسئله به کار می‌گیری.";
    let retain = hi(n,"DI")
      ? "با تکرار منظم و تمرین، دانش را پایدار در ذهن نگه می‌داری."
      : "دانش را وقتی بهتر می‌سپاری که با کنجکاوی و درگیری عملی همراه شود.";
    return [proc, solve, retain].join(" ");
  }

  function decision(n) {
    let rational = hi(n,"D")
      ? "تصمیم‌هایت عمدتاً بر پایهٔ تحلیل، منطق و سنجش پیامدها شکل می‌گیرد."
      : lo(n,"D") ? "ارزش‌ها، احساسات و تأثیر تصمیم بر آدم‌ها نقش پررنگی در انتخاب‌هایت دارند."
      : "میان منطق و احساس تعادل برقرار می‌کنی.";
    let risk = hi(n,"R")
      ? "ریسک‌پذیر هستی و از فرصت‌های نامطمئن نمی‌ترسی."
      : lo(n,"R") ? "محتاط هستی و امنیت و قطعیت را بر شانس بزرگ ترجیح می‌دهی."
      : "ریسک‌های حساب‌شده می‌پذیری اما بی‌گدار به آب نمی‌زنی.";
    let speed = hi(n,"ST")
      ? "پیش از تصمیم، چند گام جلوتر را شبیه‌سازی می‌کنی."
      : "بیشتر بر اساس موقعیت حال و واقعیت پیشِ‌رو تصمیم می‌گیری.";
    return [rational, risk, speed].join(" ");
  }

  /* ---------- Humor layer (derived from real scores) ---------- */

  function archetype(n) {
    // Priority-ordered archetypes triggered by score combinations
    const rules = [
      [hi(n,"CR")&&lo(n,"DI"), "اژدهای بیش‌فکر، مدیرعامل پروژه‌های نیمه‌تمام"],
      [hi(n,"CU")&&lo(n,"DI"), "ماشین ایده‌سازی بدون دکمهٔ توقف"],
      [hi(n,"ST")&&lo(n,"DI"), "متخصص برنامه‌ریزی برای فردا (همیشه فردا)"],
      [hi(n,"O")&&hi(n,"CU")&&lo(n,"C"), "کاشفِ همه‌چیز، استادِ هیچ‌چیز (هنوز!)"],
      [hi(n,"DI")&&hi(n,"C")&&lo(n,"E"), "اژدهای منظمِ گوشه‌نشین با چک‌لیستِ بی‌نقص"],
      [hi(n,"L")&&hi(n,"R"), "ژنرالِ بی‌باکی که اول می‌پرد بعد نقشه می‌کشد"],
      [hi(n,"E")&&lo(n,"DI"), "ستارهٔ جمع، فراری از ددلاین"],
      [hi(n,"S")&&hi(n,"DI"), "اژدهای خونسردِ کارراه‌انداز"],
      [lo(n,"S")&&hi(n,"CR"), "نابغهٔ حساسِ پر از دنیای درونی"],
      [hi(n,"D")&&hi(n,"ST"), "تحلیل‌گرِ سرد و حسابگرِ شطرنج‌باز"],
      [lo(n,"E")&&hi(n,"O"), "فیلسوفِ ساکتِ پر از جهان‌های موازی"],
    ];
    for (const [cond, title] of rules) if (cond) return title;
    return "اژدهای متعادل، استادِ راه میانه";
  }

  function funny(n) {
    const pool = [
      [hi(n,"CR")&&lo(n,"DI"), "اگر روزی تمام ایده‌هایی که شروع کرده‌ای را کامل کنی، احتمالاً قوانین فیزیک نیاز به بازنگری خواهند داشت."],
      [hi(n,"CU"), "مرورگرت احتمالاً بیشتر از مغزت تَب باز دارد، و این یعنی تو هرگز خسته نمی‌شوی از فهمیدن."],
      [lo(n,"DI"), "«از فردا جدی شروع می‌کنم» را آن‌قدر گفته‌ای که شایستهٔ یک جایزهٔ ادبی است."],
      [hi(n,"ST"), "تو برای رسیدن از مبل به یخچال هم سه سناریوی جایگزین داری."],
      [hi(n,"S")&&hi(n,"DI"), "اگر دنیا فردا تمام شود، تو احتمالاً اول کارهای امروزت را تیک می‌زنی."],
      [lo(n,"S"), "ذهنت یک تیم کامل دارد که فقط وظیفه‌اش نگرانی دربارهٔ چیزهایی است که هرگز اتفاق نمی‌افتند."],
      [hi(n,"E"), "سکوت برای تو نه آرامش، بلکه یک باگ نرم‌افزاری است که باید فوراً رفعش کنی."],
      [lo(n,"E"), "باتری اجتماعی‌ات سریع‌تر از گوشی‌ات خالی می‌شود، و شارژرش «تنهایی» نام دارد."],
      [hi(n,"R"), "کلمهٔ «احتیاط» در فرهنگ لغت تو با مداد نوشته شده، آن‌هم کم‌رنگ."],
      [hi(n,"O")&&hi(n,"CR"), "اگر «شاید این‌طوری بهتر شود» را در هر جمله نگویی، حالت خوب نیست."],
      [hi(n,"L"), "حتی در صف نانوایی هم احتمالاً ناخودآگاه یک سیستم نوبت‌دهی طراحی کرده‌ای."],
      [hi(n,"C"), "بی‌نظمیِ دیگران برایت دردِ فیزیکی واقعی ایجاد می‌کند."],
    ];
    return pick(pool, 3, 5, "تو آن‌قدر متعادلی که حتی تست‌های شخصیتی هم کمی گیج می‌شوند — و این خودش یک ابرقدرت است.");
  }

  /* ---------- helpers ---------- */
  function pick(pool, min, max, fallback) {
    const hits = pool.filter(p => p[0]).map(p => p[1]);
    if (hits.length < min && fallback) hits.push(fallback);
    return hits.slice(0, max);
  }

  return { build };
})();
